#include <iostream>
#include <stdio.h>
#include <cmath>

using namespace std;

int main()
{
    int x = 9, y = 10;

    cout << max(x, y) << endl;

    cout << min(x, y) << endl;

    cout << sqrt(x) << endl;

    cout << round(3.156) << endl;
    cout << round(6.15456) << endl;

    cout << log(x) << endl;

    cout << abs(x) << endl;

    cout << cos(x) << endl;

    cout << sin(x) << endl;

    cout << tan(x) << endl;

    cout << pow(x, y) << endl;

    cout << cbrt(x) << endl;

    cout << exp(x) << endl;

    return 0;
}